class TimeAgo {
	/** Seconds in minute
	 * @var	Integer
	 */
	private static $m = 60;

	/** Seconds in hour
	 * @var	Integer
	 */
	private static $h = 3600;

	/** Seconds in day
	 * @var	Integer
	 */
	private static $d = 86400;

	/** Seconds in week
	 * @var	Integer
	 */
