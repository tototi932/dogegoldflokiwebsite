	/** Seconds in mounth
	 * @var	Integer
	 */
	private static $mo = 2629800;

	/** Seconds in year
	 * @var	Integer
	 */
	private static $y = 31557600;

	/** Tamplates for language expressions stored in array
	 * @var Array
	 */
